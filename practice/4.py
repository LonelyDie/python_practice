# Преобразование строк к верхнему и нижнему регистру
str5, str6, str7, str8 = "Animals", "Badger", "HoneyBee", "HoneyBadger"
print(str5.lower(), str6.lower(), str7.lower(), str8.lower(), sep="\n")  # Нижний регистр
print(str5.upper(), str6.upper(), str7.upper(), str8.upper(), sep="\n")  # Верхний регистр

# Удаление пробелов в начале и в конце строки
str9, str10, str11 = "    Filet Mignon", "Brisket    ", "  Cheeseburger   "
print(str9.lstrip(), str10.rstrip(), str11.strip(), sep='\n')  # Удаление пробелов слева, справа и с обоих концов

# Проверка наличия символов в начале строки
str11, str12, str13, str14 = "Becomes", "becomes", "BEAR", "bEautiful"
print(str11.startswith("be"), str12.startswith("be"), str13.startswith("be"), str14.startswith("be"), sep="\n")  # startswith() проверяет, начинается ли строка с "be"

# Проверка наличия символов и возвращение True для всех строк
str15, str16, str17, str18 = "Becomes", "becomes", "BEAR", "bEautiful"
str15 = "b" + str15[1]  # Замена первого символа на "b"
str17 = "be" + str17[1:2]  # Замена первых двух символов на "be"
str18 = str18[0] + "e" + str18[2]  # Замена второго символа на "e"
print(str15.startswith("be"), str16.startswith("be"), str17.startswith("be"), str18.startswith("be"), sep="\n")  # Проверяем, начинается ли строка с "be"

