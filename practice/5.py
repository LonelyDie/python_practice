# Вывод числа с разделителями-подчеркиваниями и без
num1 = 25000000  # Число без разделителей
num2 = 25_000_000  # Число с разделителями для удобства чтения
print(num1, num2, sep='\n')  # Выводим оба числа на отдельных строках

# Литерал с плавающей точкой
num = 1.75e5  # Запись числа с плавающей точкой в научной нотации (1.75 * 10^5)
print(num)  # Выводим число

# Вывод минимальной степени бесконечности
num = 2e309  # Число, превышающее пределы чисел с плавающей точкой, вызовет бесконечность
print(num)  # Выводим бесконечность (infinity)

# Возведение первого числа в степень, заданную вторым числом
num1 = float(input("Enter a base: "))  # Вводим основание степени
num2 = int(input("Enter an exponent: "))  # Вводим показатель степени
num3 = num1**num2  # Возводим основание в степень
print(f"{num1} to the power of {num2} = {num3}")  # Выводим результат возведения в степень

# Абсолютное значение этого числа
num1 = float(input("Enter a number: "))  # Вводим число
num2 = abs(num1)  # Находим абсолютное значение числа
print(f"The absolute value of {num1} is {num2}")  # Выводим абсолютное значение числа

# Является ли разность двух чисел целым числом и разность которых не является целым числом
num1 = float(input("Enter a base: "))  # Вводим первое число
num2 = float(input("Enter another number: "))  # Вводим второе число
num3 = num1 - num2  # Находим разность между двумя числами
num4 = num3.is_integer()  # Проверяем, является ли разность целым числом
print(f"The difference between {num1} and {num2} is an integer? {num4}!")  # Выводим, является ли разность целым числом

# Округление до 2-х цифр
num1 = float(input("Enter a base: "))  # Вводим число
num2 = round(num1, 2)  # Округляем число до 2-х знаков после запятой
print(f"{num1} rounded to 2 decimal places is {num2}")  # Выводим округлённое число

# Вывод фиксированной точки с тремя знаками в дробной части
num = 3**0.125  # Вычисляем корень из числа 3 (3^(1/8))
print(f"{num:.3f}")  # Выводим результат с 3 знаками после запятой

# Вывод с двумя знаками в дробной части
num = 150000  # Число для вывода
print(f"{num:,.2f}")  # Выводим число с двумя знаками после запятой, разделяя тысячи запятыми

# Вывод в % без дробной части
num = 2/10  # Делаем деление
print(f"{num:.0%}")  # Выводим результат в виде процента без дробной части (умножаем на 100 и выводим как целое число)
