# Температура по шкале Фаренгейта
def convert_far_to_cel(F):
    C = (F - 32) * 5 / 9  # Преобразуем температуру из Фаренгейта в Цельсий
    print(f"{F} degrees F = {C:.2f} degrees C")  # Выводим результат с округлением до двух знаков
    return F  # Возвращаем введенную температуру в Фаренгейтах

F = int(input("Enter a temperature in degrees F: "))  # Вводим температуру в Фаренгейтах
convert_far_to_cel(F)  # Вызываем функцию для преобразования температуры

# Температура по шкале Цельсия
def convert_cel_to_far(C):
    F = C * 9 / 5 + 32  # Преобразуем температуру из Цельсия в Фаренгейт
    print(f"{C} degrees C = {F:.2f} degrees F")  # Выводим результат с округлением до двух знаков
    return F  # Возвращаем температуру в Фаренгейтах

C = int(input("Enter a temperature in degrees C: "))  # Вводим температуру в Цельсиях
