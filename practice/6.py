# Возведение в 3-ю степень
def cube(x):
    product = pow(x, 3)  # Возводим число в третью степень с помощью функции pow
    return product  # Возвращаем результат возведения в степень

a = int(input())  # Вводим число
num = cube(a)  # Вызываем функцию cube для возведения числа в степень
print(num)  # Выводим результат

