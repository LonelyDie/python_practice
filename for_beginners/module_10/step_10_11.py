last_index = text.rfind("h")  # –ù–∞—Ö–æ–¥–∏–º –ø–æ—Å–ª–µ–¥–Ω–µ–µ –≤—Ö–æ–∂–¥–µ–Ω–∏–µ "h"

# –§–æ—Ä–º–∏—Ä—É–µ–º —Å—Ç—Ä–æ–∫—É: —á–∞—Å—Ç—å –¥–æ –ø–µ—Ä–≤–æ–≥–æ "h", –ø–µ—Ä–µ–≤–µ—Ä–Ω—É—Ç–∞—è —Å–µ—Ä–µ–¥–∏–Ω–∞, —á–∞—Å—Ç—å –ø–æ—Å–ª–µ –ø–æ—Å–ª–µ–¥–Ω–µ–≥–æ "h"
result = text[:first_index + 1] + text[first_index + 1:last_index][::-1] + text[last_index:]

print(result)

