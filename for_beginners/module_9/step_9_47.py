text = input()  # –í–≤–æ–¥ —Å—Ç—Ä–æ–∫–∏
decoded_text = ""  # –ò—Ç–æ–≥–æ–≤—ã–π —Ä–∞—Å—à–∏—Ñ—Ä–æ–≤–∞–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç
i = 0  # –ò–Ω–¥–µ–∫—Å –≤ —Å—Ç—Ä–æ–∫–µ

while i < len(text):  # –ü–æ–∫–∞ –Ω–µ –¥–æ—à–ª–∏ –¥–æ –∫–æ–Ω—Ü–∞ —Å—Ç—Ä–æ–∫–∏
    if text[i : i + 3] == "[u-":  # –ï—Å–ª–∏ –Ω–∞—à–ª–∏ –Ω–∞—á–∞–ª–æ –∫–æ–Ω—Å—Ç—Ä—É–∫—Ü–∏–∏ [u-
        i += 3  # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º "[u-"
        number = ""  # –ó–¥–µ—Å—å –±—É–¥–µ–º –Ω–∞–∫–∞–ø–ª–∏–≤–∞—Ç—å —á–∏—Å–ª–æ
        while text[i] != "]":  # –ü–æ–∫–∞ –Ω–µ –≤—Å—Ç—Ä–µ—Ç–∏–º –∑–∞–∫—Ä—ã–≤–∞—é—â—É—é —Å–∫–æ–±–∫—É
            number += text[i]  # –î–æ–±–∞–≤–ª—è–µ–º —Ü–∏—Ñ—Ä—É –∫ —á–∏—Å–ª—É
            i += 1  # –ü–µ—Ä–µ—Ö–æ–¥–∏–º –∫ —Å–ª–µ–¥—É—é—â–µ–º—É —Å–∏–º–≤–æ–ª—É
        i += 1  # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º –∑–∞–∫—Ä—ã–≤–∞—é—â—É—é —Å–∫–æ–±–∫—É "]"
        decoded_text += chr(
            int(number)
        )  # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —á–∏—Å–ª–æ –≤ –±—É–∫–≤—É –∏ –¥–æ–±–∞–≤–ª—è–µ–º –≤ –∏—Ç–æ–≥–æ–≤—ã–π —Ç–µ–∫—Å—Ç
    else:
        decoded_text += text[i]  # –ï—Å–ª–∏ —ç—Ç–æ –æ–±—ã—á–Ω—ã–π —Å–∏–º–≤–æ–ª, –ø—Ä–æ—Å—Ç–æ –¥–æ–±–∞–≤–ª—è–µ–º –µ–≥–æ
        i += 1  # –ü–µ—Ä–µ—Ö–æ–¥–∏–º –∫ —Å–ª–µ–¥—É—é—â–µ–º—É —Å–∏–º–≤–æ–ª—É

print(decoded_text)  # –í—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç

