# –°—á–∏—Ç—ã–≤–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∫–Ω–∏–≥
n = int(input())

# –°—á–∏—Ç—ã–≤–∞–µ–º –ø–µ—Ä–≤—É—é –∫–Ω–∏–≥—É
prev_book = input().strip()
# –†–∞–∑–±–∏–≤–∞–µ–º —Å—Ç—Ä–æ–∫—É –Ω–∞ —Ñ–∞–º–∏–ª–∏—é –∞–≤—Ç–æ—Ä–∞ –∏ –Ω–∞–∑–≤–∞–Ω–∏–µ –∫–Ω–∏–≥–∏
last_name, rest = prev_book.split(' ', 1)
title = rest.split('¬ª')[0][1:]

# –§–ª–∞–≥, –∫–æ—Ç–æ—Ä—ã–π –±—É–¥–µ—Ç –ø–æ–∫–∞–∑—ã–≤–∞—Ç—å, –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω—ã –ª–∏ –∫–Ω–∏–≥–∏
is_sorted = True

# –°—á–∏—Ç—ã–≤–∞–µ–º –æ—Å—Ç–∞–ª—å–Ω—ã–µ –∫–Ω–∏–≥–∏
for _ in range(1, n):
    book = input().strip()
    # –†–∞–∑–±–∏–≤–∞–µ–º —Å—Ç—Ä–æ–∫—É –Ω–∞ —Ñ–∞–º–∏–ª–∏—é –∞–≤—Ç–æ—Ä–∞ –∏ –Ω–∞–∑–≤–∞–Ω–∏–µ –∫–Ω–∏–≥–∏
    last_name2, rest2 = book.split(' ', 1)
    title2 = rest2.split('¬ª')[0][1:]

    # –ï—Å–ª–∏ —Ç–µ–∫—É—â–∞—è –∫–Ω–∏–≥–∞ –¥–æ–ª–∂–Ω–∞ –∏–¥—Ç–∏ —Ä–∞–Ω—å—à–µ –ø—Ä–µ–¥—ã–¥—É—â–µ–π, –º–µ–Ω—è–µ–º —Ñ–ª–∞–≥
    if (last_name > last_name2) or (last_name == last_name2 and title > title2):
        is_sorted = False
        break

    # –û–±–Ω–æ–≤–ª—è–µ–º –∑–Ω–∞—á–µ–Ω–∏—è –¥–ª—è —Å–ª–µ–¥—É—é—â–µ–π –∏—Ç–µ—Ä–∞—Ü–∏–∏
    last_name, title = last_name2, title2

# –í—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
if is_sorted:
    print("YES")
else:
    print("NO")

