# Инициализация переменных для минимальной и максимальной строки
min_string = None
max_string = None

# Чтение 4 строк
for _ in range(4):
    line = input()

    # Обновляем минимальную и максимальную строки
    if min_string is None or line < min_string:
        min_string = line
    if max_string is None or line > max_string:
        max_string = line

# Находим Unicode-коды последних символов
min_last_char_unicode = ord(min_string[-1])
max_last_char_unicode = ord(max_string[-1])

# Вычисляем "волшебное" число
magic_number = (min_last_char_unicode * max_last_char_unicode) ** 2

# Выводим результат
print(magic_number)

