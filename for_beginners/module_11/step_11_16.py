n = int(input())  # Вводим количество чисел (n >= 2)
numbers = [int(input()) for _ in range(n)]  # Считываем n чисел в список

sums = [
    numbers[i] + numbers[i + 1] for i in range(n - 1)
]  # Складываем соседние элементы

print(sums)  # Выводим результат


