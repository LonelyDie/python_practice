n = int(input())  # –í–≤–æ–¥ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ —Å—Ç—Ä–æ–∫
array = []

# –í–≤–æ–¥ —Å–∞–º–∏—Ö —Å—Ç—Ä–æ–∫
