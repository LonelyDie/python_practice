n = int(input())
has_cool = False
count = 0
while n != 0:
    last_digit = n % 10
    if last_digit >= count:
        has_cool = True
        count = last_digit
    else:
        has_cool = False
        break
    n = n // 10
if has_cool == True:
    print("YES")
else:
    print("NO")


