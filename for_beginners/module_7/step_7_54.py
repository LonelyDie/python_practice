a, b = int(input()), int(input())
max_sum = 0  # –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è —Å—É–º–º–∞ –¥–µ–ª–∏—Ç–µ–ª–µ–π
number = 0   # –ß–∏—Å–ª–æ —Å –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π —Å—É–º–º–æ–π –¥–µ–ª–∏—Ç–µ–ª–µ–π

for i in range(a, b + 1):
    total = sum(j for j in range(1, i + 1) if i % j == 0)  # –°—á–∏—Ç–∞–µ–º —Å—É–º–º—É –¥–µ–ª–∏—Ç–µ–ª–µ–π
    if total > max_sum or (total == max_sum and i > number):
        max_sum = total
        number = i

print(number, max_sum)



