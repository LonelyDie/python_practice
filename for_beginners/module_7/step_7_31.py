total = 0
a = int(input())
while a >= 25:
    total += 1
    a = a - 25
while a >= 10:
    total += 1
    a = a - 10
while a >= 5:
    total += 1
    a = a - 5
while a >= 1:
    total += 1
    a = a - 1
print(total)


